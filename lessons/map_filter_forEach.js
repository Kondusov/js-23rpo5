// Основные различия:
// .forEach()(): Используется для выполнения действия с каждым элементом
// (например, вывод в консоль, запись в БД). Не создает новый массив, возвращает undefined.
// .map()(): Трансформирует массив. Создает новый массив той же длины,
// где каждый элемент— результат функции.
// .filter()(): Фильтрует данные. Создает новый массив, включая только элементы,
// для которых callback вернул true.

const numbers = [1, 2, 3, 4, 5];

// forEach: вывод элементов
numbers.forEach(num => console.log(num * 2)); 

// map: создание массива удвоенных чисел
const doubled = numbers.map(num => num * 2); // [2, 4, 6, 8, 10]

// filter: создание массива четных чисел
const evens = numbers.filter(num => num % 2 === 0); // [2, 4]
